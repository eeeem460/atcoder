int main() {
    INCANT;
    ll n;
    cin >> n;
    vector<ll> u(n - 1), v(n - 1);
    vector<pair<ll, ll>> w(n - 1);
    for (ll i = 0; i < n - 1; i++) {
        cin >> u[i] >> v[i] >> w[i].first;
        w[i].second = i;
        u[i]--, v[i]--;
    }

    sort(w.begin(), w.end());

    dsu d(n);

    ll ans = 0;
    for (ll i = 0; i < n - 1; i++) {
        auto [weight, index] = w[i];
        ll p1 = u[index], p2 = v[index];
        ans += weight * d.size(p1) * d.size(p2);
        d.merge(p1, p2);
    }

    cout << ans << endl;
}

